package jjjjj.loops;

import java.util.Scanner;

public class whileLoop {
    public static void main(String[] args) {
    Scanner x=new Scanner(System.in);
    System.out.println("enter limit for applying condition");
    int a=x.nextInt();
    int i=0;
    while(i<=a){
        System.out.println(a);
        i++;

    }
System.out.println();

}
    
}
