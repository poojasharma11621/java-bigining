package jjjjj.loops;

import java.util.Scanner;


/*for loop
 * for(initialisation;condition;inc/dec){
 * Sysout()}
 */


/**
 * forloops
 */
public class forloops {
    public static void main(String[] args) {
    Scanner x=new Scanner(System.in);
    int a=x.nextInt();
    for(int i=0;i<=a;i++){
        System.out.println(a);
    }
    System.out.println();
        
}


    
}